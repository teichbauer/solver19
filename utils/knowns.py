def verify_sat(sat):
    print(f"verifying {sat}")
    succ = True
    from center import Center
    for kn, vk in Center.orig_vkdic.items():
        if vk.hit(sat):
            succ = False
            print(f"kn violated. sat failed on this.")
    return succ